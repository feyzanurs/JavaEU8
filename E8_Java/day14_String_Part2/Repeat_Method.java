package day14_String_Part2;

public class Repeat_Method {
    public static void main(String[] args) {
        String str="Cydeo";
        String str2= str.repeat(4);
        System.out.println(str2);

        String s1="Wooden Spoon";
        String s2= s1.repeat(10);
        System.out.println(s2);
    }
}
